/**
Arithmetic operators

    + Plus
    - Apart from
    * Core
    / Divide
    % Divide by remainder
    ++ Increment 1 numeric value
    -- Decrease by 1 numeric value

*/


// + Plus
var a = 1;
var b = 2;
var c = a + b;
console.log(c);


// - Apart from
var a = 1;
var b = 2;
var c = a - b;
console.log(c);


// * Core
var a = 1;
var b = 2;
var c = a * b;
console.log(c);


// / Divide
var a = 1;
var b = 2;
var c = a / b;
console.log(c);


// % Divide by remainder
var a = 1;
var b = 2;
var c = a % b;
console.log(c);


// ++ Increment 1 numeric value

var a = 5;
a++;
console.log(a);  // --> a = 6;


// -- Decrease by 1 numeric value
var a = 5;
a--;
console.log(a);  // --> a = 4;